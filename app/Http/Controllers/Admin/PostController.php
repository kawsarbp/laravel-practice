    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        try {
            $postDelete = Post::find($id);
            $postDelete->delete();
            return redirect()->back()->with(['type' => 'success', 'message' => 'Post Delete success']);
        } catch (Exception $exception) {
            return redirect()->back()->with(['type' => 'danger', 'message' => 'Post Not Delete success']);
        }
